    private void BotonGuardarContactoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonGuardarContactoMouseClicked
        // Obtener los datos escritos por el usuario
        String nombre = TextoNombre.getText();//Obtiene el texto que el usuario escribió en el campo "Nombre"
        String apellidos = TextoApellido.getText();
        String telefono = TextoTelefono.getText();
        String grupo = TextoGrupo.getText();
        String correo = TextoCorreo.getText();
        //crear un nuevo contacto con estos datos obtenidos, creamos un objeto contacto con los datos ingresados
        Contacto nuevoContacto = new Contacto(nombre, apellidos, telefono, grupo, correo);
        //Agregar el contacto a la agenda
        agenda.agregarContacto(nuevoContacto);
        
        //Limpiar los campos después de guardar para que se pueda crear otro contacto.
        TextoNombre.setText("");
        TextoApellido.setText("");
        TextoTelefono.setText("");
        TextoGrupo.setText("");
        TextoCorreo.setText("");

        actualizarTotalContactos();
    }//GEN-LAST:event_BotonGuardarContactoMouseClicked

