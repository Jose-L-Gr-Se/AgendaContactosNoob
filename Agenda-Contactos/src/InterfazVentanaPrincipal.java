    // Método para actualizar la lista de contactos mostrada
    private void actualizarListaContactos(ArrayList<Contacto> contactosAMostrar) {
        DefaultListModel<String> modeloLista = new DefaultListModel<>();
        for (Contacto contacto : contactosAMostrar) {
            modeloLista.addElement(contacto.getNombre() + " " + contacto.getApellidos() + " - " + contacto.getTelefono());
        }
        ListaContactos.setModel(modeloLista);
        actualizarTotalContactos();
    }
    
    // Método para buscar contactos
    private void buscarContactos() {
        String criterioBusqueda = TextoBusqueda.getText().trim();
        if (criterioBusqueda.isEmpty()) {
            // Si no hay criterio de búsqueda, mostrar todos los contactos
            agenda.mostrarAgenda();
            actualizarListaContactos(agenda.buscarContactos(""));
        } else {
            ArrayList<Contacto> resultados = agenda.buscarContactos(criterioBusqueda);
            actualizarListaContactos(resultados);
            if (resultados.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron contactos que coincidan con la búsqueda.");
            }
        }
    }
    
    // Método para actualizar el contador de contactos
    private void actualizarTotalContactos() {
        EtiquetaTotalContactos.setText("Total contactos: " + agenda.getTotalContactos());
    }
